/* Map an arcade tile code to a ZPac tile index.
 * Creates the "garbage" visual effect using existing ZPac tiles. */
static uint16_t map_arcade_to_zpac_tile(uint8_t arc_tile) {
    /* Dot tiles — critical for gameplay */
    if (arc_tile == 0x10) {
        return FONT_DOT;  /* the '.' character for dot visual */
    }
    if (arc_tile == 0x14) {
        return FONT_0;  /* energizer — won't appear, but just in case */
    }

    /* Blank/black */
    if (arc_tile == 0x00 || arc_tile == 0x40) {
        return TILE_BLANK;
    }

    /* Digits 1-9 */
    if (arc_tile >= 0x01 && arc_tile <= 0x09) {
        return FONT_0 + arc_tile;
    }
    /* A-F */
    if (arc_tile >= 0x0A && arc_tile <= 0x0F) {
        return FONT_A + (arc_tile - 0x0A);
    }

    /* 0x11-0x1F: font-like garbage */
    if (arc_tile >= 0x11 && arc_tile <= 0x1F) {
        return FONT_A + ((arc_tile - 0x11) % 26);
    }

    /* 0x20-0x3F: text area codes — map to letters/digits */
    if (arc_tile >= 0x20 && arc_tile <= 0x39) {
        return FONT_A + ((arc_tile - 0x20) % 26);
    }
    if (arc_tile >= 0x3A && arc_tile <= 0x3F) {
        return FONT_0 + ((arc_tile - 0x3A) % 10);
    }

    /* 0x41-0x5A: text-like */
    if (arc_tile >= 0x41 && arc_tile <= 0x5A) {
        return FONT_A + (arc_tile - 0x41);
    }
    if (arc_tile >= 0x5B && arc_tile <= 0x5F) {
        return FONT_DASH;
    }

    /* 0x60-0x9F: maze tile fragments */
    if (arc_tile >= 0x60 && arc_tile <= 0x9F) {
        return (uint16_t)(arc_tile - 0x60) % 177;
    }

    /* 0xA0-0xBF: fruit tile range — map to maze fragments */
    if (arc_tile >= 0xA0 && arc_tile <= 0xBF) {
        return (uint16_t)((arc_tile - 0xA0) % 64) + 50;
    }

    /* 0xC0-0xFF: high range — map to maze fragments */
    return (uint16_t)(arc_tile % 177);
}

void level256_apply(void) {
    uint8_t row, ci, i;

    is_level_256 = 1;

    /* === Part 1: Corrupt the logic map (right half, cols 14-27) ===
     * All cells become walkable (no walls). Only 8 cells have dots. */
    for (row = 0; row < 31; row++) {
        for (ci = 0; ci < 14; ci++) {
            uint8_t col = 14 + ci;
            maze_logic[row][col] = CELL_WALKABLE;
        }
    }

    /* Place the 8 dots */
    for (i = 0; i < NUM_256_DOTS; i++) {
        maze_logic[dot_row[i]][dot_col[i]] = CELL_WALKABLE | CELL_DOT;
    }

    /* Recalculate dot_count: right half originally had 122 dots,
     * we replaced them with 8. So: dot_count = dot_count - 122 + 8 */
    if (dot_count > 114) {
        dot_count -= 114;
    } else {
        dot_count = 8;  /* safety */
    }

    /* === Part 2: Corrupt the visual tilemap (right half) ===
     * Overwrite the ZVB tilemap for the maze area, cols 14-27. */
    for (row = 0; row < 31; row++) {
        for (ci = 0; ci < 14; ci++) {
            uint8_t col = 14 + ci;
            uint8_t vis_x = (uint8_t)((col * 12 + 6) >> 4);
            uint8_t vis_y = (uint8_t)((row * 12 + 6) >> 4);
            uint8_t scr_x = MAZE_OFFSET_X + vis_x;
            uint8_t scr_y = MAZE_OFFSET_Y + vis_y;
            uint8_t arc_tile_code = arcade_tiles[row][ci];
            uint16_t zpac_tile = map_arcade_to_zpac_tile(arc_tile_code);
            uint8_t zpac_pal;
            {
                uint8_t pb = arcade_pal_packed[row * 7 + (ci >> 1)];
                zpac_pal = (ci & 1) ? (pb & 0x0F) : (pb >> 4);
            }
            uint8_t attr = (zpac_pal << 4);
            if (zpac_tile >= 256) attr |= 0x01;
            gfx_tilemap_place(&vctx, (uint8_t)(zpac_tile & 0xFF), 0, scr_x, scr_y);
            gfx_tilemap_place(&vctx, attr, 1, scr_x, scr_y);
        }
    }
}
